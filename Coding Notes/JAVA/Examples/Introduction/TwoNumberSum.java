package Ficha2;

public class TwoNumberSum {

	public static void main(String[] args) {
		int firstValue=10;
		int secondValue=23;
		int sum=0;
		
		sum = firstValue + secondValue;
		
		// Exemplo usando o println:
		System.out.println(firstValue + " + " + secondValue + " = " + sum);
		
		// Exemplo usando o printf:
		System.out.printf("%d + %d = %d\n", firstValue, secondValue, sum);
	}

}
